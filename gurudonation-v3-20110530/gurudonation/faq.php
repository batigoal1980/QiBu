					<a id="basics" name="basics"></a>

					<h1 class="section">Basics</h1>

					<h2>What is CrowdFunding?</h2>

					<p class="a">CrowdFunding is a service to easily collect money through the Internet.</p>

					<h2>Who can use CrowdFunding?</h2>

					<p class="a">Everyone who needs to raise money for a group purchase, charity, a personal CrowdFunding, a birthday or wedding present, and so on.</p>

					<h2>How does CrowdFunding work?</h2>

					<p class="a">1. You create a CrowdFunding page and set the target amount you need to raise.<br />

								 2. Send the URL of the CrowdFunding Page to the people who want to collect money from. <br />

								 3. People go to your CrowdFunding Page and send you the money with their credit card.</p>

					<h2>Is CrowdFunding an investment mechanism?</h2>

					<p class="a">No. All money transfers are CrowdFundings and there is no return for contributors.</p>

					<h2>Is it secure?</h2>

					<p class="a">The CrowdFunding payment process is completely secure. The money transfers and all sensible operations are managed by the secure and popular <a href="http://www.paypal.com/" target="_blank" title="Go to PayPal">PayPal service</a>. </p>

					<h2>Do you save my credit card information?</h2>

					<p class="a">PayPal processes all of the transactions on CrowdFunding. No one sees your credit card information besides PayPal, not even us.</p>

					<h2>What fees do CrowdFunding charge?</h2>

					<p class="a">Currently, <strong>the service is free</strong>! People send money directly on your PayPal account. PayPal may charge you some <a href="http://www.paypal.com/cgi-bin/webscr?cmd=_display-fees-outside" title="View PayPal fees" target="_blank">processing fees</a>.</p>

					<h2>Who is behind CrowdFunding?</h2>

					<p class="a">CrowdFunding is a startup of Tailmedia, an Italian company specialized in Web 2.0. Tailmedia also created <a href="http://en.qualeteatro.com" rel="nofollow" target="_blank">QualeTeatro</a>, the search engine for the Italian Theatre.</p>

					<a id="sending" name="sending"></a>

					<h1 class="section">Sending Money</h1>

					<h2>Do I have to register with CrowdFunding to send money?</h2>

					<p class="a">No, you don’t need to register with CrowdFunding to send a contribution.</p>

					<h2>How much can I send?</h2>

					<p class="a">It depends on the CrowdFunding. Some CrowdFunding allow free contributions, so you can send any amount you want. Some CrowdFundings allow only set amounts of money transfers; for example: $10, $25, or $50. </p>

					<h2>Which payment methods do you accept?</h2>

					<p class="a">You send money through PayPal, so usually through your credit card. However, you can use any payment method accepted by PayPal. </p>

					<h2>Do I have to be a PayPal customer to send money?</h2>

					<p class="a">No, you just need a credit card.</p>

					<h2>If I make a contribution, when will my money be withdrawn?</h2>

					<p class="a">When you make a contribution, your credit card or PayPal account is charged immediately.</p>

					<a id="receiving" name="receiving"></a>

					<h1 class="section">Receiving Money</h1>

					<h2>How much can I raise?</h2>

					<p class="a">Each CrowdFunding can raise up to $1,000,000.</p>

					<h2>Can I change the target amount of a CrowdFunding once it has started?</h2>

					<p class="a">Yes, you can change it by the <a href="myCrowdFundings.php" target="_blank" title="Go to My CrowdFundings">My CrowdFundings page</a>.</p>

					<h2>Is there a maximum number of contributors allowed for a CrowdFunding?</h2>

					<p class="a">There is no maximum number of contributors allowed for a CrowdFunding.</p>

					<a id="public-private" name="public-private"></a>

					<h2>What’s the difference between a Public and Private CrowdFunding?</h2>

					<p class="a">By default, a CrowdFunding Page is "private", meaning it is not 

					visible to search engines. This is done via the <a target="_blank" title="Go to Wikipedia page about HTML Meta elements" href="http://en.wikipedia.org/wiki/Meta_element#The_robots_attribute">the HTML Meta robots attribute</a>. Only people who know the URL address can navigate to the CrowdFunding Page. This protects your privacy. It is entirely up to you to spread the word and share the URL with your friends. </p>

					<p class="a">When you create a CrowdFunding, you can also choose to set it as "public": in this case Google and other search engines will index the page and it may show up in their results. Choosing this option will attract even more people and potential contributors.</p>

					<a id="public-tips" name="public-tips"></a>

					<h2>How can a Public CrowdFunding succeed?</h2>

					<p class="a">When you create a Public CrowdFunding, you have to spread the word over the Internet and convince people to donate. Here are some tips:</p>

					<ul class="a">

						<li>Insert an accurate <strong>description</strong>. Describe who you are and why people should trust you.</li>

						<li>Insert a <strong>link to your website</strong>, so that people can check it out.</li>

						<li>Insert a <strong>custom image</strong>.</li>    

						<li><strong>Invite your Facebook friends</strong> to contribute: you find the command in the <a title="Go to My CrowdFundings" href="myCrowdFundings.php">My CrowdFundings</a> page.</li>

						<li><strong>Link your CrowdFunding page from your website</strong> or write a post about it on your blog; prove the CrowdFunding is really yours. You find some ready-to-use badges for your website on the CrowdFunding Page.</li>

					</ul>

					<h2>CrowdFundings have duration. How long can a CrowdFunding last?</h2>

					<p class="a">The set duration of a CrowdFunding page is an incentive to people to contribute immediately. A CrowdFunding can last up to 1 year. The time limit is set by the CrowdFunding creator when a CrowdFunding is started<!--, and does not change -->. Once the CrowdFunding is expired, people can’t send you any more money throught the CrowdFunding Page.</p>

					<h2>What if a CrowdFunding reaches its target amount before time expires?</h2>

					<p class="a">If a CrowdFunding reaches its target amount before its time expires, CrowdFunding will continue to accept contributions until the full time period expires.</p>

					<h2>What if a CrowdFunding don't collect its target amount in time?</h2>

					<p class="a">The CrowdFunding page remains online, but stops accepting contributions. Of course, the money you raised is still yours! You can use the partial raised amount for your need. And you can start again the CrowdFunding to try to collect more money.</p>

					<h2>How can I see the list and details of contributors?</h2>

					<p class="a">Since all money transfer are managed by PayPal, you can view the list and details of CrowdFundings from the PayPal site. </p>

					<h2>What kind of CrowdFundings are acceptable?</h2>

					<p class="a">PayPal processes all contributions on CrowdFunding, so CrowdFundings must fall under the <a target="_blank" title="Go to PayPal Legal Agreement" href="http://www.paypal.com/cgi-bin/webscr?cmd=p/gen/ua/ua-outside">PayPal Legal Agreement</a>.</p>

					<h2>Can I leave CrowdFunding at any time?</h2>

					<p class="a">Yes, you can. From <a href="myaccount.php" title="Go to My Account">My Account</a>, choose Delete Account.</p>

